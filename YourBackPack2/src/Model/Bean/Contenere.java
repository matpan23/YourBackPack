package Model.Bean;

public class Contenere {

    private String carrello;
    private String Zaino;
    private int numQuantita;

    public Contenere() {
    }

    public String getCarrello() {
        return carrello;
    }

    public void setCarrello(String carrello) {
        this.carrello = carrello;
    }

    public String getZaino() {
        return Zaino;
    }

    public void setZaino(String Zaino) {
        this.Zaino = Zaino;
    }

    public int getNumQuantita() {
        return numQuantita;
    }

    public void setNumQuantita(int numQuantita) {
        this.numQuantita = numQuantita;
    }


}